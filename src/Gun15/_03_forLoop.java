package Gun15;

public class _03_forLoop {
    public static void main(String[] args) {

//        0 dan 10 (dahil) a kadar sayıları ekrana yazdırınız.
//        10 dan 0 (dahil) a kadar sayıları ekrana yazdırınız. (ayrı bir diğer for)

        for(int j=0;j<=10;j++)
            System.out.println("f_j = " + j);


        for(int i=10; i>=0; i--)
            System.out.println("i = " + i);



    }
}
